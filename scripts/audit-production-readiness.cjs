const fs = require('fs');
const path = require('path');

const root = process.cwd();
let failed = false;

function fail(message) {
  failed = true;
  console.error(`FAIL: ${message}`);
}

function pass(message) {
  console.log(`PASS: ${message}`);
}

function exists(rel) {
  return fs.existsSync(path.join(root, rel));
}

function read(rel) {
  return fs.readFileSync(path.join(root, rel), 'utf8');
}

const requiredDocs = [
  'FIXDEV_AGENT_SKILL.md',
  'MODULE_REGISTRY.md',
  'ROLE_MENU_MATRIX.md',
  'VALIDATION_CHECKLIST.md',
  'PRODUCTION_READINESS_GATE.md',
  'ACCEPTANCE_CRITERIA_BY_MODULE.md',
  'E2E_TEST_PLAN.md',
  'VISUAL_REGRESSION_CHECKLIST.md',
  'DATABASE_SCHEMA_GUARDRAIL.md',
  'DEPLOYMENT_PRODUCTION_CHECKLIST.md',
  'CUSTOMER_PORTAL_RULES.md',
  'MOBILE_PWA_RULES.md',
  'PERFORMANCE_SCALABILITY_RULES.md',
  'INCIDENT_ROLLBACK_PLAN.md',
  'RELEASE_SIGNOFF_TEMPLATE.md'
];

console.log('=== FIXDEV Production Readiness Audit ===');

for (const doc of requiredDocs) {
  if (exists(doc)) pass(`${doc} exists`);
  else fail(`${doc} is missing`);
}

if (!exists('package.json')) {
  fail('package.json is missing');
} else {
  const pkg = JSON.parse(read('package.json'));
  const scripts = pkg.scripts || {};
  const requiredScripts = ['lint', 'build'];
  for (const s of requiredScripts) {
    if (scripts[s]) pass(`package.json script '${s}' exists`);
    else fail(`package.json script '${s}' missing`);
  }

  for (const [name, cmd] of Object.entries(scripts)) {
    const matches = String(cmd).matchAll(/node\s+(scripts\/[^\s"']+)/g);
    for (const match of matches) {
      const rel = match[1];
      if (exists(rel)) pass(`script '${name}' target exists: ${rel}`);
      else fail(`script '${name}' points to missing file: ${rel}`);
    }
  }

  if (pkg.name && /react-example|vite|demo/i.test(pkg.name)) {
    fail(`package name looks like placeholder: ${pkg.name}`);
  } else {
    pass('package name does not look like default placeholder');
  }
}

if (exists('.env')) {
  console.warn('WARN: .env exists in working tree; ensure it is not committed and does not contain production secrets in shared packages');
} else {
  pass('.env not present in current package root');
}

if (exists('.env.example')) {
  const envExample = read('.env.example');
  const suspicious = /(service_role|password|secret|token|key)\s*=\s*['"]?(?!your_|example|placeholder|xxx|changeme|$)[A-Za-z0-9_\-.]{16,}/i.test(envExample);
  if (suspicious) console.warn('WARN: .env.example may contain real-looking secrets - review manually');
  else pass('.env.example does not contain obvious real secrets');
} else {
  console.warn('WARN: .env.example is missing - recommended to create for documentation');
}

if (exists('node_modules')) console.warn('WARN: node_modules exists; do not commit or ship it in source zip');
else pass('node_modules not present');

if (exists('dist')) console.warn('WARN: dist exists; ensure it is generated by build and not stale');
else pass('dist not present before build');

const sourceRoots = ['src', 'server.ts', 'vite.config.ts', 'tsconfig.json'];
for (const item of sourceRoots) {
  if (exists(item)) pass(`${item} exists`);
  else fail(`${item} missing`);
}

const srcText = exists('src')
  ? fs.readdirSync(path.join(root, 'src'), { recursive: true })
      .filter(f => /\.(ts|tsx|js|jsx)$/.test(String(f)))
      .slice(0, 500)
      .map(f => {
        try { return fs.readFileSync(path.join(root, 'src', f), 'utf8'); }
        catch { return ''; }
      }).join('\n')
  : '';

const mustMention = [
  ['tenant', /tenant/i],
  ['service/tiket', /service|servis|tiket/i],
  ['POS/kasir', /pos|kasir|sale|penjualan/i],
  ['inventory/stok', /inventory|stok|stock/i],
  ['settings/pengaturan', /settings|pengaturan/i]
];

for (const [label, pattern] of mustMention) {
  if (pattern.test(srcText)) pass(`source mentions ${label}`);
  else console.warn(`WARN: source does not clearly mention ${label}`);
}

if (failed) {
  console.error('\nPRODUCTION READINESS AUDIT: FAIL');
  process.exit(1);
}

console.log('\nPRODUCTION READINESS AUDIT: PASS');
